    for ($i = 0; $i -lt $hostCount; $i++) {
        Write-Host ''
        Write-Host "域名入口 #$($i + 1)" -ForegroundColor Cyan
        $defaultItem = if ($i -lt $defaultHostSites.Count) { $defaultHostSites[$i] } else { $null }
        $siteKey = Prompt-Text -Label '  站点标识' -Default ([string](Get-PropValue -Object $defaultItem -Name 'site_key' -Default "host_$($i + 1)"))
        $displayName = Prompt-Text -Label '  站点名称' -Default ([string](Get-PropValue -Object $defaultItem -Name 'display_name' -Default "域名站点 $($i + 1)"))
        $hostsDefault = (As-Array -Value (Get-PropValue -Object $defaultItem -Name 'hosts' -Default @())) -join ','
        $hostsCsv = Prompt-Text -Label '  域名列表(逗号分隔)' -Default $hostsDefault
        $hostNames = @($hostsCsv.Split(',') | ForEach-Object { $_.Trim().ToLowerInvariant().TrimEnd('.') } | Where-Object { $_ }) | Select-Object -Unique

        $defaultFrontend = Parse-UpstreamUrl -Url ([string](Get-PropValue -Object $defaultItem -Name 'frontend_upstream' -Default (Get-PropValue -Object $defaultItem -Name 'frontend_base_url' -Default ''))) -FallbackHost $frontendIpValue -FallbackPort $frontendPortValue
        $defaultBackend = Parse-UpstreamUrl -Url ([string](Get-PropValue -Object $defaultItem -Name 'backend_upstream' -Default (Get-PropValue -Object $defaultItem -Name 'backend_base_url' -Default ''))) -FallbackHost $backendIpValue -FallbackPort $backendPortValue

        $hostFrontendIp = Prompt-Text -Label '  前端回源 IP' -Default $defaultFrontend.Host
