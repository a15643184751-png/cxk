            frontend_upstream = [string](Get-PropValue -Object $item -Name 'frontend_upstream' -Default (Get-PropValue -Object $item -Name 'frontend_base_url' -Default ''))
            backend_upstream = [string](Get-PropValue -Object $item -Name 'backend_upstream' -Default (Get-PropValue -Object $item -Name 'backend_base_url' -Default ''))
        }
    }
