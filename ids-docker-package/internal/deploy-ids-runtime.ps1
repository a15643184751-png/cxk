        $hostBackendPort = Prompt-Port -Label '  后端回源端口' -Default $defaultBackend.Port

        $hostMappings += [ordered]@{
            site_key = $siteKey
            display_name = $displayName
            hosts = $hostNames
            frontend_upstream = (Build-UpstreamUrl -HostName $hostFrontendIp -Port $hostFrontendPort)
            backend_upstream = (Build-UpstreamUrl -HostName $hostBackendIp -Port $hostBackendPort)
        }
    }
