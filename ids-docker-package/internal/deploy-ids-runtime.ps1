function To-IntOrDefault {
    param(
        $Value,
        [int]$Default
    )

    if ($null -eq $Value) {
        return $Default
    }

    $parsed = 0
    if ([int]::TryParse([string]$Value, [ref]$parsed) -and $parsed -ge 1 -and $parsed -le 65535) {
        return $parsed
    }

    return $Default
}

function Parse-UpstreamUrl {
    param(
        [string]$Url,
        [string]$FallbackHost,
        [int]$FallbackPort
    )

    if ([string]::IsNullOrWhiteSpace($Url)) {
        return @{
            Host = $FallbackHost
            Port = $FallbackPort
        }
    }

    $candidate = $Url.Trim()
    if ($candidate -notmatch '^[a-zA-Z][a-zA-Z0-9+\-.]*://') {
        $candidate = "http://$candidate"
    }

    try {
        $uri = [System.Uri]$candidate
        return @{
            Host = $uri.Host
            Port = $(if ($uri.IsDefaultPort) { $FallbackPort } else { $uri.Port })
        }
    } catch {
        return @{
            Host = $FallbackHost
            Port = $FallbackPort
        }
    }
}

function Build-UpstreamUrl {
    param(
        [string]$HostName,
        [int]$Port
    )

    return "http://$($HostName.Trim()):$Port"
}

function Convert-JsonArrayText {
    param(
        $Items
    )

    $rows = As-Array -Value $Items
    if ($rows.Count -eq 0) {
        return '[]'
    }

    $serialized = foreach ($item in $rows) {
        $item | ConvertTo-Json -Depth 12 -Compress
    }

    return '[' + ($serialized -join ',') + ']'
}

function Normalize-PortMappings {
    param(
        $Items
    )

    $rows = @()
    foreach ($item in (As-Array -Value $Items)) {
        if ($null -eq $item) {
            continue
        }

        $siteKey = [string](Get-PropValue -Object $item -Name 'site_key' -Default '')
        $displayName = [string](Get-PropValue -Object $item -Name 'display_name' -Default '')
        $ingressPort = To-IntOrDefault -Value (Get-PropValue -Object $item -Name 'ingress_port' -Default 0) -Default 0
        $frontendUpstream = [string](Get-PropValue -Object $item -Name 'frontend_upstream' -Default (Get-PropValue -Object $item -Name 'frontend_base_url' -Default ''))
        $backendUpstream = [string](Get-PropValue -Object $item -Name 'backend_upstream' -Default (Get-PropValue -Object $item -Name 'backend_base_url' -Default ''))

        if ([string]::IsNullOrWhiteSpace($siteKey) -or [string]::IsNullOrWhiteSpace($displayName)) {
            continue
        }
        if ($ingressPort -lt 1) {
            continue
        }
        if ([string]::IsNullOrWhiteSpace($frontendUpstream) -or [string]::IsNullOrWhiteSpace($backendUpstream)) {
            continue
        }

        $rows += [ordered]@{
            site_key = $siteKey.Trim()
            display_name = $displayName.Trim()
            ingress_port = $ingressPort
            frontend_upstream = $frontendUpstream.Trim()
            backend_upstream = $backendUpstream.Trim()
        }
    }

    return @($rows)
}

function Normalize-HostMappings {
    param(
        $Items
    )

    $rows = @()
    foreach ($item in (As-Array -Value $Items)) {
        if ($null -eq $item) {
            continue
        }

        $siteKey = [string](Get-PropValue -Object $item -Name 'site_key' -Default '')
        $displayName = [string](Get-PropValue -Object $item -Name 'display_name' -Default '')
        $frontendUpstream = [string](Get-PropValue -Object $item -Name 'frontend_upstream' -Default (Get-PropValue -Object $item -Name 'frontend_base_url' -Default ''))
        $backendUpstream = [string](Get-PropValue -Object $item -Name 'backend_upstream' -Default (Get-PropValue -Object $item -Name 'backend_base_url' -Default ''))
        $hosts = @((As-Array -Value (Get-PropValue -Object $item -Name 'hosts' -Default @())) | ForEach-Object {
            ([string]$_).Trim().ToLowerInvariant().TrimEnd('.')
        } | Where-Object { $_ }) | Select-Object -Unique

        if ([string]::IsNullOrWhiteSpace($siteKey) -or [string]::IsNullOrWhiteSpace($displayName)) {
            continue
        }
        if ($hosts.Count -eq 0) {
            continue
        }
        if ([string]::IsNullOrWhiteSpace($frontendUpstream) -or [string]::IsNullOrWhiteSpace($backendUpstream)) {
            continue
        }

        $rows += [ordered]@{
            site_key = $siteKey.Trim()
            display_name = $displayName.Trim()
            hosts = $hosts
            frontend_upstream = $frontendUpstream.Trim()
            backend_upstream = $backendUpstream.Trim()
        }
    }

    return @($rows)
}

function Prompt-Text {
    param(
        [string]$Label,
        [string]$Default = '',
        [switch]$AllowEmpty
    )

    if ($UseDefaults) {
        return [string]$Default
    }

    while ($true) {
        $suffix = if ([string]::IsNullOrWhiteSpace($Default)) { '' } else { " [$Default]" }
        $value = Read-Host "$Label$suffix"
        if ([string]::IsNullOrWhiteSpace($value)) {
            if ($AllowEmpty) {
                return ''
            }
            if (-not [string]::IsNullOrWhiteSpace($Default)) {
                return $Default
            }
            Write-Host '请输入内容。' -ForegroundColor Yellow
            continue
        }
        return $value.Trim()
    }
}

function Prompt-Port {
    param(
        [string]$Label,
        [int]$Default
    )

    if ($UseDefaults) {
        return [int]$Default
    }

    while ($true) {
        $value = Read-Host "$Label [$Default]"
        if ([string]::IsNullOrWhiteSpace($value)) {
            return [int]$Default
        }

        $parsed = 0
        if ([int]::TryParse($value.Trim(), [ref]$parsed) -and $parsed -ge 1 -and $parsed -le 65535) {
            return $parsed
        }

        Write-Host '端口必须是 1-65535 之间的整数。' -ForegroundColor Yellow
    }
}

function Prompt-Count {
    param(
        [string]$Label,
        [int]$Default = 0
    )

    if ($UseDefaults) {
        return $Default
    }

    while ($true) {
        $value = Read-Host "$Label [$Default]"
        if ([string]::IsNullOrWhiteSpace($value)) {
            return $Default
        }

        $parsed = 0
        if ([int]::TryParse($value.Trim(), [ref]$parsed) -and $parsed -ge 0) {
            return $parsed
        }

        Write-Host '请输入大于等于 0 的整数。' -ForegroundColor Yellow
    }
}

function Prompt-Bool {
    param(
        [string]$Label,
        [bool]$Default
    )

    if ($UseDefaults) {
        return $Default
    }

    $defaultText = if ($Default) { 'Y/n' } else { 'y/N' }

    while ($true) {
        $value = Read-Host "$Label [$defaultText]"
        if ([string]::IsNullOrWhiteSpace($value)) {
            return $Default
        }

        switch ($value.Trim().ToLowerInvariant()) {
            'y' { return $true }
            'yes' { return $true }
            '1' { return $true }
            'n' { return $false }
            'no' { return $false }
            '0' { return $false }
            default {
                Write-Host '请输入 y 或 n。' -ForegroundColor Yellow
            }
        }
    }
}

function Ensure-UniquePorts {
    param(
        [int]$GatewayPortValue,
        [System.Collections.IEnumerable]$PortMappings
    )

    $seen = New-Object System.Collections.Generic.HashSet[int]
    [void]$seen.Add($GatewayPortValue)

    foreach ($item in $PortMappings) {
        $port = [int](Get-PropValue -Object $item -Name 'ingress_port' -Default 0)
        if ($port -lt 1) {
            continue
        }
        if (-not $seen.Add($port)) {
            throw "检测到重复入口端口: $port"
        }
    }
}

function Ensure-UniqueHosts {
    param(
        [System.Collections.IEnumerable]$HostMappings
    )

    $seen = New-Object System.Collections.Generic.HashSet[string]
    foreach ($item in $HostMappings) {
        foreach ($host in (As-Array -Value (Get-PropValue -Object $item -Name 'hosts' -Default @()))) {
            $normalized = ([string]$host).Trim().ToLowerInvariant().TrimEnd('.')
            if ([string]::IsNullOrWhiteSpace($normalized)) {
                continue
            }
            if (-not $seen.Add($normalized)) {
                throw "检测到重复域名入口: $normalized"
            }
        }
    }
}

